.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC FAR
LEA AX, @DATA
MOV DS, AX
MOV AH, 1
INT 21H
MOV BL, AL
CMP BL, 49
JE DISPLAY_SUMMATION
CMP BL, 50
JE DISPLAY_SUBTRACTION
CMP BL, 51
JE DISPLAY_INPUT
JMP DISPLAY_CHRACTER
DISPLAY_SUMMATION:
MOV AH, 2
MOV DL, 10
INT 21H
MOV DL, 13
INT 21H
MOV CL, 2
ADD CL, 3
ADD CL, 48
MOV DL, CL
INT 21H
JMP EXIT
DISPLAY_SUBTRACTION:
MOV AH, 2
MOV DL, 10
INT 21H
MOV DL, 13
INT 21H
MOV CL, 4
SUB CL, 3
ADD CL, 48
MOV DL, CL
INT 21H
JMP EXIT
DISPLAY_INPUT:
MOV AH, 2
MOV DL, 10
INT 21H
MOV DL, 13
INT 21H
MOV DL, BL
INT 21H
JMP EXIT
DISPLAY_CHRACTER:
MOV AH, 2
MOV DL, 10
INT 21H
MOV DL, 13
INT 21H
MOV DL, 97
INT 21H
JMP EXIT
EXIT:
MOV AH, 4CH
INT 21H
MAIN ENDP
END MAIN