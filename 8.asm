.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC FAR
LEA AX, @DATA
MOV DS, AX
MOV AH, 1
INT 21H
CMP AL, 50
JGE INPUT_CHARACTER
JMP CHECK
INPUT_CHARACTER:
CMP AL, 57
JG CHECK
MOV AH, 2
MOV DL, 10
INT 21H
MOV DL, 13
INT 21H
MOV AH, 1
INT 21H
MOV BL, AL
MOV AH, 2
MOV DL, 10
INT 21H
MOV DL, 13
INT 21H
MOV DL, BL
INT 21H
JMP EXIT
CHECK:
CMP AL, 99
JE PRINT_C
JMP PRINT_N
PRINT_C:
MOV AH, 2
MOV DL, 10
INT 21H
MOV DL, 13
INT 21H
MOV DL, 99
INT 21H
JMP EXIT
PRINT_N:
MOV AH, 2
MOV DL, 10
INT 21H
MOV DL, 13
INT 21H
MOV DL, 110
INT 21H
JMP EXIT
EXIT:
MOV AH, 4CH
INT 21H
MAIN ENDP
END MAIN