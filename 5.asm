.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC FAR
LEA AX, @DATA
MOV DS, AX
MOV CL, 30
CHECK:
CMP CL, 10
JE PRINT_NEW_LINE
CMP CL, 20
JE PRINT_NEW_LINE
JMP PRINT
PRINT:
MOV AH, 2
MOV DL, 97
INT 21H
SUB CL, 1
CMP CL, 0
JE EXIT
JMP CHECK
PRINT_NEW_LINE:
MOV AH, 2
MOV DL, 10
INT 21H
MOV DL, 13
INT 21H
JMP PRINT
EXIT:
MOV AH, 4CH
INT 21H
MAIN ENDP
END MAIN