.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC FAR
LEA AX, @DATA
MOV DS, AX
MOV AH, 1
INT 21H
MOV DL, AL
CMP DL, 49
JE PRINT_O
CMP DL, 51
JE PRINT_O
CMP DL, 50
JE PRINT_E
CMP DL, 52
JE PRINT_E
PRINT_O:
MOV AH, 2
MOV DL, 10
INT 21H
MOV Dl, 13
INT 21H
MOV DL, 111
INT 21H
JMP EXIT
PRINT_E:
MOV AH, 2
MOV DL, 10
INT 21H
MOV Dl, 13
INT 21H
MOV DL, 101
INT 21H
JMP EXIT
EXIT:
MOV AH, 4CH
INT 21H
MAIN ENDP
END MAIN